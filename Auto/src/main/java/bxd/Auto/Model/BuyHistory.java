package bxd.Auto.Model;

public class BuyHistory {
	private String[] buyArray;
	private String winSum;
	private String buySum;
	private String firstQiHao;
	
	public String getFirstQiHao() {
		return firstQiHao;
	}
	public void setFirstQiHao(String firstQiHao) {
		this.firstQiHao = firstQiHao;
	}
	public String[] getBuyArray() {
		return buyArray;
	}
	public void setBuyArray(String[] buyArray) {
		this.buyArray = buyArray;
	}
	public String getWinSum() {
		return winSum;
	}
	public void setWinSum(String winSum) {
		this.winSum = winSum;
	}
	public String getBuySum() {
		return buySum;
	}
	public void setBuySum(String buySum) {
		this.buySum = buySum;
	}
	
}
