package bxd.Auto.Model;

public class Plan {
	private String topGame;
	private String newGameID;
	private String newGameMultiple;
	private String newGameNum;
	private String[] gameList;
	
	
	public String getTopGame() {
		return topGame;
	}
	public void setTopGame(String topGame) {
		this.topGame = topGame;
	}
	public String getNewGameID() {
		return newGameID;
	}
	public void setNewGameID(String newGameID) {
		this.newGameID = newGameID;
	}
	public String getNewGameMultiple() {
		return newGameMultiple;
	}
	public void setNewGameMultiple(String newGameMultiple) {
		this.newGameMultiple = newGameMultiple;
	}
	public String getNewGameNum() {
		return newGameNum;
	}
	public void setNewGameNum(String newGameNum) {
		this.newGameNum = newGameNum;
	}
	public String[] getGameList() {
		return gameList;
	}
	public void setGameList(String[] gameList) {
		this.gameList = gameList;
	}
	
}
